package org.streameps.filter;

import java.io.Serializable;

/**
 *
 * @author Frank Appiah
 */
public enum FilterType implements  Serializable{

    COMPARISON("comparison"),
    RANGE("range"),
    IN_NOT_VALUES("in_not values"),
    NULL("null");
    private String type;

    private FilterType(String type) {
        this.type = type;
    }

    public static FilterType getType(String filter) {
        for (FilterType filterType : FilterType.values()) {
            if (filterType.getType().equalsIgnoreCase(filter)) {
                return filterType;
            }
        }
        throw new IllegalArgumentException();
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return type;
    }

    
}
